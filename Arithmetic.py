import math


class Arithmetic:

    @staticmethod
    def sqroot(self, number):
        return math.sqrt(number)

    @staticmethod
    def addition(self, num1, num2):
        return num1 + num2

    @staticmethod
    def subtraction(self, num1, num2):
        return num1 - num2

    @staticmethod
    def multiplication(self, num1, num2):
        return num1 * num2

    @staticmethod
    def division(self, num1, num2):
        return num1 / num2
